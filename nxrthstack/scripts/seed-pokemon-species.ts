async function seed() {
  console.log("Seeding Pokemon species...\n");
  let successCount = 0;
  let errorCount = 0;

  for (const pokemon of pokemonData) {
    const [pokedexId, name, generation, types, hp, atk, def, spAtk, spDef, speed, legendary, starter] = pokemon;

    try {
      await db
        .insert(schema.pokemonSpecies)
        .values({
          pokedexId,
          name,
          generation,
          types,
          hpBase: hp,
          attackBase: atk,
          defenseBase: def,
          spAtkBase: spAtk,
          spDefBase: spDef,
          speedBase: speed,
          isLegendary: legendary,
          isStarter: starter,
          isActive: true,
        })
        .onConflictDoUpdate({
          target: schema.pokemonSpecies.pokedexId,
          set: {
            name,
            generation,
            types,
            hpBase: hp,
            attackBase: atk,
            defenseBase: def,
            spAtkBase: spAtk,
            spDefBase: spDef,
            speedBase: speed,
            isLegendary: legendary,
            isStarter: starter,
          },
        });

      successCount++;
      if (successCount % 50 === 0) {
        console.log(`  Seeded ${successCount} Pokemon...`);
      }
    } catch (error) {
      errorCount++;
      console.error(`  Error seeding ${name}:`, error);
    }
  }

  console.log("\nSeeding complete!");
  console.log(`Total Pokemon: ${pokemonData.length}`);
  console.log(`Successfully seeded: ${successCount}`);
  console.log(`Errors: ${errorCount}`);
  console.log(`\nGen 1: ${pokemonData.filter(p => p[2] === 1).length}`);
  console.log(`Gen 2: ${pokemonData.filter(p => p[2] === 2).length}`);
  console.log(`Gen 3: ${pokemonData.filter(p => p[2] === 3).length}`);
  console.log(`Legendaries: ${pokemonData.filter(p => p[9]).length}`);
  console.log(`Starters: ${pokemonData.filter(p => p[10]).length}`);
}

seed().catch(console.error);
